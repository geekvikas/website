$(document).ready(function($) {
"use strict"
/*-----------------------------------------------------------------------------------*/
/*		STICKY NAVIGATION
/*-----------------------------------------------------------------------------------*/
$(".sticky").sticky({topSpacing:0});
/*-----------------------------------------------------------------------------------*/
/* 	LOADER
/*-----------------------------------------------------------------------------------*/
$("#loader").delay(50).fadeOut("slow");
/*-----------------------------------------------------------------------------------*/
/* 	Intro Video
/*-----------------------------------------------------------------------------------*/
if(window.innerWidth >992){
	$("#intro-video").delay(5500).animate({
		opacity: 0, // animate slideUp
		width: '100px',
		height: '50px',
		top:'10px',
		left:'50px',
	  }, 'slow', 'linear', function() {
		$(this).remove();
	  });
	$("#intro-video").setAttribute("autoplay","true");
	
};

/*-----------------------------------------------------------------------------------*/
/*  FULL SCREEN
/*-----------------------------------------------------------------------------------*/
$('.full-screen').superslides({});
/*-----------------------------------------------------------------------------------*/
/* 	Counter Timer
/*-----------------------------------------------------------------------------------*/
$('.countdown').downCount({
     date: '12/12/2018 12:00:00' // M/D/Y
});
$('.countdown-all').downCount({
     date: '12/12/2021 12:00:00' // M/D/Y
});
/*-----------------------------------------------------------------------------------*/
/* 	COUNTER
/*-----------------------------------------------------------------------------------*/
$('.counter').counterUp({
    delay: 50,
    time: 2000
});
/*-----------------------------------------------------------------------------------*/
/* 	MagnificPopup
/*-----------------------------------------------------------------------------------*/
$('.popup-youtube, .popup-vimeo, .popup-gmaps').magnificPopup({
	disableOn: 700,
	type: 'iframe',
	mainClass: 'mfp-fade',
	removalDelay: 160,
	preloader: false,
	fixedContentPos: false
});

});
